    <h1>Ejemplo de operaciones comparación en PHP</h1>
    <?php
    // Este script compara las variables numéricas si son mayores, menores o iguales.
    // == significa si es igual a una variable
    // != si es diferente
    // <  si a es menor a b
    // >  si a es mayor a b
    // >= si a es mayor o igual a b
    // <= si a es menor o igual a b
    $a = 8;
    $b = 3;
    $c = 3;
    echo $a == $b, "<br>";
    echo $a != $b, "<br>";
    echo $a < $b, "<br>";
    echo $a > $b, "<br>";
    echo $a >= $c, "<br>";
    echo $a <= $c, "<br>"
    ?>
